upper_base = eval(input("Enter the upper base length of trapezium: "))
lower_base = eval(input("Enter the lower base length of trapezium: "))
height = eval(input("Enter the height of the trapezium: "))
area = (1 / 2) * (upper_base + lower_base) * height
print(f"The area of the trapezium is: {area}")
